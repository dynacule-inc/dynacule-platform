def generate_psi4_input(
    molecule: Any,
    task: str = 'single_point',
    theory: str = 'b3lyp',
    basis_set: str = '6-31g*',
    charge: int = 0,
    multiplicity: int = 1,
    extra_keywords: Optional[List[str]] = None
) -> str:
    """
    Generate a Psi4 input file string.

    Parameters
    ----------
    molecule : any
        A molecule object (we assume it has attributes for coordinates, symbols, etc.)
        For simplicity, we expect a dictionary with 'symbols' and 'coordinates' (in Angstroms).
    task : str
        Type of calculation: 'single_point', 'optimization', 'frequency'.
    theory : str
        Electronic structure theory (e.g., 'b3lyp', 'hf', 'mp2').
    basis_set : str
        Basis set (e.g., '6-31g*', 'cc-pvdz').
    charge : int
        Molecular charge.
    multiplicity : int
        Spin multiplicity (1 = singlet, 2 = doublet, etc.).
    extra_keywords : list of str, optional
        Additional keywords to add to the input (e.g., ['scf_type df']).

    Returns
    -------
    str
        The Psi4 input file content.
    """
    if extra_keywords is None:
        extra_keywords = []

    # Start building the input
    lines = []
    lines.append('# Psi4 input file generated by Dynacule')
    lines.append(f'molecule mol {{')
    lines.append(f'  {charge} {multiplicity}')
    # Assuming molecule is a dict with 'symbols' and 'coordinates'
    symbols = molecule.get('symbols', [])
    coords = molecule.get('coordinates', [])
    for symbol, coord in zip(symbols, coords):
        lines.append(f'  {symbol} {coord[0]:.6f} {coord[1]:.6f} {coord[2]:.6f}')
    lines.append('}')
    lines.append('')

    # Set options
    lines.append(f'set {{')
    lines.append(f'  basis {basis_set}')
    lines.append(f'  scf_type df')  # Default to density fitting if available
    for kw in extra_keywords:
        lines.append(f'  {kw}')
    lines.append(f'}}')
    lines.append('')

    # Task-specific sections
    if task == 'single_point':
        lines.append(f'energy({theory})')
    elif task == 'optimization':
        lines.append(f'optimize({theory})')
    elif task == 'frequency':
        lines.append(f'frequency({theory})')
    else:
        raise ValueError(f'Task {task} not supported for Psi4.')

    return '\\n'.join(lines)


def generate_orca_input(
    molecule: Any,
    task: str = 'single_point',
    theory: str = 'b3lyp',
    basis_set: str = '6-31g*',
    charge: int = 0,
    multiplicity: int = 1,
    extra_keywords: Optional[List[str]] = None,
    pal: int = 1
) -> str:
    """
    Generate an ORCA input file string.

    Parameters
    ----------
    molecule : any
        A molecule object (dict with 'symbols' and 'coordinates' in Angstroms).
    task : str
        Type of calculation: 'single_point', 'optimization', 'frequency'.
    theory : str
        Electronic structure theory (e.g., 'b3lyp', 'hf', 'mp2').
    basis_set : str
        Basis set (e.g., '6-31g*', 'def2-svp').
    charge : int
        Molecular charge.
    multiplicity : int
        Spin multiplicity.
    extra_keywords : list of str, optional
        Additional keywords (e.g., ['TightSCF']).
    pal : int
        Number of processors to use.

    Returns
    -------
    str
        The ORCA input file content.
    """
    if extra_keywords is None:
        extra_keywords = []

    lines = []
    lines.append('! ' + theory + ' ' + basis_set)
    lines.append('! ' + ' '.join(extra_keywords))
    lines.append('%pal')
    lines.append(f'  nprocs {pal}')
    lines.append('end')
    lines.append('')
    lines.append('* xyzfile {charge} {multiplicity}')
    symbols = molecule.get('symbols', [])
    coords = molecule.get('coordinates', [])
    for symbol, coord in zip(symbols, coords):
        lines.append(f'  {symbol} {coord[0]:.6f} {coord[1]:.6f} {coord[2]:.6f}')
    lines.append('*')
    
    # Add task-specific blocks
    if task == 'optimization':
        lines.append('%maxiter 50')
        lines.append('%geom')
        lines.append('  Optimize')
        lines.append('end')
    elif task == 'frequency':
        lines.append('%freq')
        lines.append('  End')
        lines.append('end')
    # For single point, no extra block needed.

    return '\\n'.join(lines)


def parse_psi4_output(output_text: str) -> Dict[str, Any]:
    """
    Parse Psi4 output file to extract energies, gradients, frequencies, etc.

    Parameters
    ----------
    output_text : str
        The content of the Psi4 output file.

    Returns
    -------
    dict
        Dictionary with keys:
        - 'energy': total electronic energy (in Hartree)
        - 'gradient': gradient vector (if available, in Hartree/Bohr)
        - 'frequencies': vibrational frequencies (in cm^-1)
        - 'normal_modes': normal modes (if available)
        - 'success': boolean indicating if calculation succeeded
    """
    result = {
        'energy': None,
        'gradient': None,
        'frequencies': None,
        'normal_modes': None,
        'success': False
    }
    
    lines = output_text.split('\\n')
    
    # Look for final energy
    for line in lines:
        if 'FINAL ENERGY' in line:
            # Example: 'FINAL ENERGY: -76.0267712546'
            try:
                energy = float(line.split(':')[1].strip())
                result['energy'] = energy
                result['success'] = True
            except:
                pass
        elif 'ENERGY=' in line and 'FINAL SINGLE POINT ENERGY' in line:
            # Another common pattern
            try:
                energy = float(line.split('=')[1].strip().split()[0])
                result['energy'] = energy
                result['success'] = True
            except:
                pass
    
    # Look for gradient (if available)
    # This is more complex; we'll look for a section after 'GRADIENT' or 'CARTESIAN GRADIENT'
    # For simplicity, we'll skip detailed gradient parsing for now.
    
    # Look for vibrational frequencies
    freq_section = False
    freqs = []
    for line in lines:
        if 'VIBRATIONAL FREQUENCIES' in line:
            freq_section = True
            continue
        if freq_section:
            if 'NORMAL MODES' in line or 'IR INTENSITIES' in line:
                # End of frequency list
                break
            # Try to parse a frequency line
            parts = line.split()
            if len(parts) >= 3:
                try:
                    # First column might be mode number, second frequency, third IR intensity
                    freq = float(parts[1])
                    freqs.append(freq)
                except ValueError:
                    pass
    if freqs:
        result['frequencies'] = np.array(freqs)
    
    return result


