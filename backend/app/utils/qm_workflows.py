def run_psi4_calculation(
    input_file: str,
    output_file: Optional[str] = None,
    scratch_dir: Optional[str] = None
) -> Tuple[bool, str]:
    """
    Run a Psi4 calculation given an input file.

    Parameters
    ----------
    input_file : str
        Path to the Psi4 input file.
    output_file : str, optional
        Path to the output file. If None, defaults to input_file with .out extension.
    scratch_dir : str, optional
        Directory for scratch files.

    Returns
    -------
    tuple (success, output_text)
        success: boolean indicating if the calculation ran without error.
        output_text: the output from the calculation (or error message).
    """
    if not psi4_available:
        return False, "Psi4 is not installed. Please install psi4 to run calculations."
    
    if output_file is None:
        output_file = os.path.splitext(input_file)[0] + '.out'
    
    # Set scratch directory if provided
    if scratch_dir:
        os.environ['PSI_SCRATCH'] = scratch_dir
    
    try:
        # Run psi4 using the Python API
        psi4.core.set_output_file(output_file, False)
        psi4.core.clean()
        psi4.core.clean_variables()
        psi4.core.clean_options()
        
        # Read the input file and execute
        with open(input_file, 'r') as f:
            input_str = f.read()
        # Use psi4 energy function? Actually, we need to parse the input to know the task.
        # For simplicity, we'll rely on the input file specifying the task.
        # We can use psi4.driver.run() but it's easier to let the input file dictate.
        # Instead, we'll run the input file as a script.
        # Note: This is a simplification; in practice, we might want to use the Python API more directly.
        exec(input_str, {'__name__': '__main__'})
        
        # Read the output file
        with open(output_file, 'r') as f:
            output_text = f.read()
        
        return True, output_text
    except Exception as e:
        return False, str(e)


def run_orca_calculation(
    input_file: str,
    output_file: Optional[str] = None,
    orca_command: str = 'orca'
) -> Tuple[bool, str]:
    """
    Run an ORCA calculation given an input file.

    Parameters
    ----------
    input_file : str
        Path to the ORCA input file.
    output_file : str, optional
        Path to the output file. If None, defaults to input_file with .out extension.
    orca_command : str
        The command to run ORCA (default 'orca', assuming it's in PATH).

    Returns
    -------
    tuple (success, output_text)
        success: boolean indicating if the calculation ran without error.
        output_text: the output from the calculation (or error message).
    """
    if output_file is None:
        output_file = os.path.splitext(input_file)[0] + '.out'
    
    # ORCA input file extension is typically .inp
    # But we can run with any extension; ORCA uses the first argument as input file.
    cmd = [orca_command, input_file]
    
    try:
        # Run ORCA and capture output
        result = subprocess.run(cmd, capture_output=True, text=True, timeout=3600)
        output_text = result.stdout + result.stderr
        
        # Write output to file (optional)
        with open(output_file, 'w') as f:
            f.write(output_text)
        
        success = (result.returncode == 0)
        return success, output_text
    except subprocess.TimeoutExpired:
        return False, "ORCA calculation timed out."
    except Exception as e:
        return False, str(e)


